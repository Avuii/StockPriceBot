export function t(language: Language, key: keyof typeof dictionary.pl) {
  return dictionary[language][key];
}

export function navLabel(language: Language, view: string) {
  if (view in dictionary[language]) {
    return dictionary[language][view as keyof typeof dictionary.pl];
  }

  return view;
}

export function productCountLabel(language: Language, count: number) {
  if (language === 'en') {
    return count === 1 ? '1 product' : `${count} products`;
  }

  if (count === 1) {
    return '1 produkt';
  }

  if (count > 1 && count < 5) {
    return `${count} produkty`;
  }

  return `${count} produktow`;
}
